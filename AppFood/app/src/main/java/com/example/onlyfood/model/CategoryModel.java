package com.example.onlyfood.model;

public class CategoryModel {
    private String _NameCategory; // Category's Name
    private String _ImageCategory; // Image's Name
    //getter Category's Name
    public String get_NameCategory() {
        return _NameCategory;
    }
    //setter Category's Name
    public void set_NameCategory(String _NameCategory) {
        this._NameCategory = _NameCategory;
    }
    //Getter Image
    public String get_ImageCategory() {
        return _ImageCategory;
    }
    //Setter Image
    public void set_ImageCategory(String _ImageCategory) {
        this._ImageCategory = _ImageCategory;
    }
}
